# Python tiene una de la sintaxis mas poderosas para manipultar secuencias
# esta sintaxis se llama slice (rebanada en espanol)

my_name = "gabriel"
my_name[0]  # g
my_name[-1]  # l
my_name[0:3]  # gab
my_name[::2]  # gbil
my_name[3:]  # riel
my_name[:]  # gabriel
my_name[3:3]  # (se muestra vacio)
my_name[1:-1:2]  # are
